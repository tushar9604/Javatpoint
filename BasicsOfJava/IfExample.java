public class IfExample {
	public static void main(String[] args){
		//defining an 'age' variable
		int age = 20;
		int number = 13;
		
		// checking the age
		if(age>18) {
			System.out.print("Age is greater than 18");
		}
		
		if(number%2==0){
			System.out.println("even number");
		} else {
			System.out.println("odd number");
		}
	}
}